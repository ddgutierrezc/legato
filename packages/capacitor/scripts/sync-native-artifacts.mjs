import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const PACKAGE_ROOT = path.resolve(__dirname, '..');
const EXPECTED_ANDROID_PUBLICATION_GROUP = 'dev.dgutierrez';

function assertString(value, fieldName) {
  if (typeof value !== 'string' || value.trim() === '') {
    throw new Error(`native-artifacts.json: ${fieldName} must be a non-empty string`);
  }
}

function isHttpsUrl(value) {
  return /^https:\/\//i.test(value);
}

function isLocalPathLike(value) {
  return /^(?:\.{1,2}[\\/]|\/|file:)/i.test(value);
}

export function validateContract(contract) {
  if (!contract || typeof contract !== 'object') {
    throw new Error('native-artifacts.json: root contract must be an object');
  }

  const { android, ios } = contract;

  if (!android || typeof android !== 'object') {
    throw new Error('native-artifacts.json: android section is required');
  }
  if (!ios || typeof ios !== 'object') {
    throw new Error('native-artifacts.json: ios section is required');
  }

  assertString(android.repositoryUrl, 'android.repositoryUrl');
  assertString(android.group, 'android.group');
  assertString(android.artifact, 'android.artifact');
  assertString(android.version, 'android.version');

  if (android.group !== EXPECTED_ANDROID_PUBLICATION_GROUP) {
    throw new Error(`native-artifacts.json: android.group must be "${EXPECTED_ANDROID_PUBLICATION_GROUP}" for publication-pipeline-v1`);
  }

  assertString(ios.packageUrl, 'ios.packageUrl');
  assertString(ios.packageName, 'ios.packageName');
  assertString(ios.product, 'ios.product');
  assertString(ios.version, 'ios.version');
  assertString(ios.versionPolicy, 'ios.versionPolicy');

  const invalidUrls = [android.repositoryUrl, ios.packageUrl].filter(
    (value) => !isHttpsUrl(value) || isLocalPathLike(value),
  );
  if (invalidUrls.length > 0) {
    throw new Error('native-artifacts.json: repository/package URLs must be HTTPS URLs (not local path refs)');
  }

  if (ios.versionPolicy !== 'exact') {
    throw new Error('native-artifacts.json: ios.versionPolicy must be "exact" for v1 policy');
  }

  return contract;
}

export function generateManagedSnippets(contract) {
  validateContract(contract);

  const coordinate = `${contract.android.group}:${contract.android.artifact}:${contract.android.version}`;

  return {
    android: [
      'ext.legatoNativeArtifactContract = [',
      `    repositoryUrl: '${contract.android.repositoryUrl}',`,
      `    coordinate: '${coordinate}'`,
      ']',
      '// Adapter Android dependency must stay artifact-only.',
      "// Do not replace with project(':native:android:core').",
    ].join('\n'),
    swift: [
      'let legatoNativeArtifactContract = (',
      `    packageUrl: "${contract.ios.packageUrl}",`,
      `    packageName: "${contract.ios.packageName}",`,
      `    product: "${contract.ios.product}",`,
      `    versionPolicy: "${contract.ios.versionPolicy}",`,
      `    version: "${contract.ios.version}"`,
      ')',
      '// iOS adapter switch-over is active: remote Swift package + exact pinning.',
      'let legatoCorePackageDependency: Package.Dependency =',
      `    .package(url: "${contract.ios.packageUrl}", exact: "${contract.ios.version}")`,
    ].join('\n'),
    readme: [
      '### Native artifact distribution contract (foundation)',
      '',
      '| Platform | Policy | Source of truth |',
      '|---|---|---|',
      `| Android | Maven Central coordinate \`${coordinate}\` | \`native-artifacts.json\` |`,
      `| iOS | SwiftPM remote package \`${contract.ios.packageName}\` at \`${contract.ios.packageUrl}\` pinned with \`exact(${contract.ios.version})\` | \`native-artifacts.json\` |`,
      '',
      '> This section is generated by `scripts/sync-native-artifacts.mjs`.',
      '> Android adapter switch-over is active (artifact coordinates only). iOS adapter switch-over is active (remote Swift package + exact pinning).',
    ].join('\n'),
  };
}

function syncIosSwiftDependency(packageSwiftSource) {
  const localPathPattern = /\.package\(path:\s*"[^\"]*LegatoCore[^\"]*"\)/;
  const remoteUrlPattern = /\.package\(url:\s*"https:\/\/github\.com\/legato\/legato-ios-core\.git",\s*exact:\s*"[^"]+"\)/;
  const dependencyLine = '        legatoCorePackageDependency';

  if (localPathPattern.test(packageSwiftSource)) {
    return packageSwiftSource.replace(localPathPattern, dependencyLine);
  }

  if (remoteUrlPattern.test(packageSwiftSource)) {
    return packageSwiftSource.replace(remoteUrlPattern, dependencyLine);
  }

  return packageSwiftSource;
}

export function applyManagedBlock(source, { startMarker, endMarker, snippet }) {
  const startIndex = source.indexOf(startMarker);
  const endIndex = source.indexOf(endMarker);

  if (startIndex === -1 || endIndex === -1 || endIndex < startIndex) {
    return `${source.trimEnd()}\n\n${startMarker}\n${snippet}\n${endMarker}\n`;
  }

  const prefix = source.slice(0, startIndex + startMarker.length);
  const suffix = source.slice(endIndex);

  return `${prefix}\n${snippet}\n${suffix}`;
}

function readJson(filePath) {
  return JSON.parse(readFileSync(filePath, 'utf8'));
}

function applyManagedFile(filePath, markers, snippet) {
  const current = readFileSync(filePath, 'utf8');
  const updated = applyManagedBlock(current, {
    startMarker: markers.begin,
    endMarker: markers.end,
    snippet,
  });
  writeFileSync(filePath, updated, 'utf8');
}

export function syncNativeArtifacts({ packageRoot = PACKAGE_ROOT } = {}) {
  const contractPath = path.join(packageRoot, 'native-artifacts.json');
  const contract = validateContract(readJson(contractPath));
  const snippets = generateManagedSnippets(contract);

  applyManagedFile(path.join(packageRoot, 'android/build.gradle'), {
    begin: '// NATIVE_ARTIFACTS:BEGIN',
    end: '// NATIVE_ARTIFACTS:END',
  }, snippets.android);

  const packageSwiftPath = path.join(packageRoot, 'Package.swift');
  const currentPackageSwift = readFileSync(packageSwiftPath, 'utf8');
  const packageSwiftWithManagedDependency = syncIosSwiftDependency(currentPackageSwift);
  writeFileSync(packageSwiftPath, packageSwiftWithManagedDependency, 'utf8');

  applyManagedFile(packageSwiftPath, {
    begin: '// NATIVE_ARTIFACTS:BEGIN',
    end: '// NATIVE_ARTIFACTS:END',
  }, snippets.swift);

  applyManagedFile(path.join(packageRoot, 'README.md'), {
    begin: '<!-- NATIVE_ARTIFACTS:BEGIN -->',
    end: '<!-- NATIVE_ARTIFACTS:END -->',
  }, snippets.readme);
}

if (process.argv[1] === __filename) {
  syncNativeArtifacts();
}
